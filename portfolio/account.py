def test_position_long_calculations():
    """Test standard LONG position calculations and status transitions."""
    pos = Position(ticker="AAPL", quantity=10, avg_price=100.0, current_price=100.0)
    assert pos.direction == "LONG"
    assert pos.unrealized_pnl == 0.0
    assert pos.market_value == 1000.0

    pos.current_price = 110.0
    assert pos.unrealized_pnl == 100.0
    assert pos.market_value == 1100.0


def test_position_short_calculations():
    """Test standard SHORT position calculations and status transitions."""
    pos = Position(ticker="TSLA", quantity=-5, avg_price=200.0, current_price=200.0)
    assert pos.direction == "SHORT"
    assert pos.unrealized_pnl == 0.0
    assert pos.market_value == 1000.0

    pos.current_price = 180.0
    assert pos.unrealized_pnl == 100.0
    assert pos.market_value == 900.0


def test_position_average_cost_addition():
    """Test average cost calculation when buying more shares."""
    pos = Position(ticker="NVDA")
    # Buy 10 at 100
    pnl1 = pos.update_position(10, 100.0)
    assert pnl1 == 0.0
    assert pos.quantity == 10
    assert pos.avg_price == 100.0

    # Buy 10 more at 120
    pnl2 = pos.update_position(10, 120.0)
    assert pnl2 == 0.0
    assert pos.quantity == 20
    assert pos.avg_price == 110.0


def test_position_reduction_and_close():
    """Test position reduction, P&L realization, and closure."""
    pos = Position(ticker="MSFT")
    # Buy 10 at 300
    pos.update_position(10, 300.0)

    # Sell 4 at 320 (reduce long)
    pnl_reduction = pos.update_position(-4, 320.0)
    assert pnl_reduction == 80.0  # (320 - 300) * 4
    assert pos.quantity == 6
    assert pos.avg_price == 300.0  # Average price remains unchanged

    # Sell remaining 6 at 330 (close position)
    pnl_close = pos.update_position(-6, 330.0)
    assert pnl_close == 180.0  # (330 - 300) * 6
    assert pos.quantity == 0


def test_position_reversal():
    """Test reversing a position from LONG to SHORT and vice versa."""
    pos = Position(ticker="BTC")
    # Long 2 at 50,000
    pos.update_position(2, 50000.0)

    # Sell 5 at 55,000 (closes 2 long, opens 3 short)
    pnl_reverse = pos.update_position(-5, 55000.0)
    assert pnl_reverse == 10000.0  # (55000 - 50000) * 2
    assert pos.quantity == -3
    assert pos.avg_price == 55000.0
    assert pos.direction == "SHORT"


def test_account_calculations_spot():
    """Test account equity, balance, and margin tracking in a 100% margin (Spot) account."""
    acct = Account(initial_capital=10000.0, margin_requirement=1.0)
    assert acct.equity == 10000.0
    assert acct.cash == 10000.0
    assert acct.free_margin == 10000.0

    # Buy 50 AAPL at 100, commission = 10
    success = acct.apply_fill("AAPL", "BUY", 50, 100.0, 10.0)
    assert success
    assert acct.total_commission == 10.0
    assert acct.balance == 9990.0  # Cash balance only changes with commissions/realized P&L
    assert acct.used_margin == 5000.0  # 50 shares * 100 price * 1.0 margin
    assert acct.unrealized_pnl == 0.0
    assert acct.equity == 9990.0
    assert acct.free_margin == 4990.0

    # Price increases to 110
    acct.update_market_price("AAPL", 110.0)
    assert acct.unrealized_pnl == 500.0  # (110 - 100) * 50
    assert acct.equity == 10490.0  # 9990 + 500
    assert acct.used_margin == 5500.0  # 50 * 110 * 1.0
    assert acct.free_margin == 4990.0

    # Sell 50 AAPL at 110, commission = 10
    acct.apply_fill("AAPL", "SELL", 50, 110.0, 10.0)
    assert acct.total_realized_pnl == 500.0
    assert acct.balance == 10480.0  # 9990 + 500 realized - 10 commission
    assert acct.equity == 10480.0
    assert acct.used_margin == 0.0
    assert acct.free_margin == 10480.0
    assert "AAPL" not in acct.positions


def test_account_leverage_margin_call():
    """Test account leveraged buying and margin call detection."""
    # 10% Margin requirement (10x Leverage)
    acct = Account(initial_capital=1000.0, margin_requirement=0.1)

    # Buy 90 AAPL at 100, commission = 5
    acct.apply_fill("AAPL", "BUY", 90, 100.0, 5.0)
    assert acct.balance == 995.0
    assert acct.used_margin == 900.0  # 90 * 100 * 0.1
    assert acct.free_margin == 95.0  # 995 - 900
    assert not acct.is_margin_called

    # Price drops to 90
    acct.update_market_price("AAPL", 90.0)
    # Unrealized P&L = (90 - 100) * 90 = -900
    # Equity = 995 - 900 = 95
    # Used Margin = 90 * 90 * 0.1 = 810
    # Equity (95) < Used Margin (810) -> Margin Call!
    assert acct.is_margin_called


