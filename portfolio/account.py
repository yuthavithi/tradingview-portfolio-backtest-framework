def run_examples():
    """Demonstrates portfolio management usage examples with full logs."""
    print("--- Running Portfolio Backtest Account Example ---")
    # Initialize a leveraged trading account (e.g. 5x leverage / 20% margin requirement)
    account = Account(initial_capital=5000.0, margin_requirement=0.2)

    # 1. Open Long position on Ticker 'AAPL'
    account.apply_fill(ticker="AAPL", action="BUY", quantity=50, price=150.0, commission=15.0)

    # 2. Open Short position on Ticker 'BTC'
    account.apply_fill(ticker="BTC", action="SELL", quantity=0.1, price=60000.0, commission=5.0)

    # 3. Simulate market updates
    print("\nUpdating market prices...")
    account.update_market_price(ticker="AAPL", price=155.0)  # AAPL goes up (gaining on long)
    account.update_market_price(ticker="BTC", price=62000.0)  # BTC goes up (losing on short)

    # Print status
    print(f"Current Account Equity: ${account.equity:.2f}")
    print(f"Current Unrealized P&L: ${account.unrealized_pnl:.2f}")
    print(f"Used Margin: ${account.used_margin:.2f}")
    print(f"Free Margin: ${account.free_margin:.2f}")
    print(f"Margin Level: {account.margin_level:.1f}%")

    # 4. Close the BTC Short position (with loss)
    print("\nClosing short position on BTC...")
    account.apply_fill(ticker="BTC", action="BUY", quantity=0.1, price=62000.0, commission=5.0)

    # 5. Sell partial AAPL Long position (with profit)
    print("\nReducing long position on AAPL...")
    account.apply_fill(ticker="AAPL", action="SELL", quantity=25, price=160.0, commission=10.0)

    # Final Account State
    print(f"\nFinal Account Balance: ${account.balance:.2f}")
    print(f"Final Total Realized PnL: ${account.total_realized_pnl:.2f}")
    print(f"Final Total Commissions Paid: ${account.total_commission:.2f}")
    print(f"Remaining active positions: {list(account.positions.keys())}")


if __name__ == "__main__":
    # If run directly, run usage examples
    run_examples()
