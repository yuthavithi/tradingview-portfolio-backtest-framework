def run_example() -> None:
    """Demonstrates RiskEngine position sizing and boundary limits."""
    print("--- Running Risk Engine Example ---")

    params = RiskParameters(
        sizing_mode="risk_per_trade",
        risk_pct=0.02,                   # Risk 2% per trade
        max_concurrent_positions=2,      # Limit concurrent positions to 2
        max_margin_usage_pct=0.50,       # Limit margin usage to 50%
        leverage=5.0                     # 5x leverage
    )
    
    risk_engine = RiskEngine(params)
    equity = 10000.0

    # 1. Position Sizing Examples
    # Risk Per Trade sizing
    size_risk = risk_engine.calculate_position_size(
        equity=equity, 
        entry_price=100.0, 
        stop_loss_pct=0.05
    )
    print(f"Risk Per Trade Size (2% risk, 5% stop): {size_risk:.2f} contracts")

    # Change to Kelly Sizing
    risk_engine.params.sizing_mode = "kelly"
    size_kelly = risk_engine.calculate_position_size(
        equity=equity,
        entry_price=100.0,
        win_rate=0.55,
        win_loss_ratio=1.5
    )
    print(f"Kelly Size (55% win rate, 1.5 win/loss ratio, 0.5 fraction): {size_kelly:.2f} contracts")

    # Change back to Risk Per Trade for validation checks
    risk_engine.params.sizing_mode = "risk_per_trade"
    
    # 2. Risk Limit Validation Examples
    t = datetime(2026, 1, 1, 10, 0, 0)
    
    # Check first valid entry
    allowed1 = risk_engine.validate_trade_entry(
        ticker="AAPL",
        quantity=size_risk,
        price=100.0,
        equity=equity,
        active_positions_count=0,
        current_margin_used=0.0,
        current_portfolio_risk=0.0,
        timestamp=t,
        stop_loss_pct=0.05
    )
    print(f"Enter AAPL position allowed? {allowed1}")
    
    # Check entry violating concurrent positions limit (Active positions = 2)
    allowed2 = risk_engine.validate_trade_entry(
        ticker="TSLA",
        quantity=size_risk,
        price=200.0,
        equity=equity,
        active_positions_count=2,
        current_margin_used=2000.0,
        current_portfolio_risk=400.0,
        timestamp=t,
        stop_loss_pct=0.05
    )
    print(f"Enter TSLA position allowed? {allowed2}")

    # Check entry violating margin usage (Needs 8000 margin, limit is 5000)
    allowed3 = risk_engine.validate_trade_entry(
        ticker="BTC",
        quantity=200.0,
        price=200.0,
        equity=equity,
        active_positions_count=1,
        current_margin_used=1000.0,
        current_portfolio_risk=200.0,
        timestamp=t,
        stop_loss_pct=0.05
    )
    print(f"Enter BTC position allowed? {allowed3}")

    # Print summary Risk Report
    report = risk_engine.generate_report(initial_equity=10000.0, ending_equity=9800.0)
    report.print_summary()


if __name__ == "__main__":
    run_example()
